#include "../Header/ft_printf.h"

int	ft_putnbr(int n)
{
	int	i;

	i = 0;
	if (n == -2147483648)
	{
		i = ft_putstr("-2147483648");
		return (i);
	}
	else
	{
		if (n < 0)
		{
			i += ft_putchar('-');
			n = -n;
		}
		if (n > 9)
		{
			i += ft_putnbr(n / 10);
		}
		i += ft_putchar((n % 10 + '0'));
	}
	return (i);
}
